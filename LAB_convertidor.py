# Datos de entrada
millas = 7.38
kilometros = 12.25

# Conversión de millas a kilómetros
km = millas * 1.61

# Conversión de kilómetros a millas
mi = kilometros / 1.61

# Imprimir los resultados utilizando la función print() con múltiples argumentos
print(round(millas, 2), "millas son", round(km, 2), "kilómetros")
print(round(kilometros, 2), "kilómetros son", round(mi, 2), "millas")
