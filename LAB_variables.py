# Crear las variables con el número de manzanas que tenía cada persona
john = 3  # Juan tiene 3 manzanas
mary = 5  # María tiene 5 manzanas
adam = 6  # Adán tiene 6 manzanas

# Imprimir las variables en una sola línea separadas por comas
print(john, mary, adam)

# Crear la variable total_apples con la suma de las tres variables anteriores
total_apples = john + mary + adam

# Imprimir el valor de total_apples
print("Total de manzanas:", total_apples)

# Experimentando con otras operaciones aritméticas
suma = john + mary + adam
resta = john - mary
multiplicacion = john * mary
division = john / adam
division_entera = john // adam
residuo = john % adam
potencia = john ** 2

# Imprimir los resultados de las operaciones
print("Suma de manzanas:", suma)
print("Resta de manzanas (Juan - María):", resta)
print("Multiplicación de manzanas:", multiplicacion)
print("División de manzanas (Juan / Adán):", division)
print("División entera de manzanas (Juan // Adán):", division_entera)
print("Residuo de manzanas (Juan % Adán):", residuo)
