# Solicitamos al usuario el número de manzanas que tiene
manzanas = int(input("¿Cuántas manzanas tienes? "))

# Calculamos el número de manzanas al cuadrado
cuadrado_manzanas = manzanas ** 2

# Calculamos el número de manzanas multiplicado por 3
triplicado_manzanas = manzanas * 3

# Mostramos los resultados
print(f"Si tienes {manzanas} manzanas, entonces:")
print(f"- El cuadrado de tus manzanas es: {cuadrado_manzanas}")
print(f"- Si multiplicamos tus manzanas por 3, el resultado es: {triplicado_manzanas}")
